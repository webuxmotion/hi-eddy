    <p>ПУБЛІЧНА ОФЕРТА
      <br>HI-EDDY (через ФОП «ПЕРЕВЕРЗЄВ АНДРІЙ ОЛЕКСАНДРОВИЧ») пропонує фізичним особам (далі – Користувач) (разом у тексті – Сторони, окремо Сторона) приєднатися до цього договору публічної оферти про надання послуг (далі – Договір) на визначених у ньому умовах та відповідно до обраного переліку послуг. Приєднанням до цього Договору також вважатимуться конклюдентні дії: проставлення галочки навпроти поля «Ознайомлений із публічною офертою» та/або оплата рахунку чи використання онлайн платіжного засобу для оплати за онлайн курси та/або отримання доступу до онлайн навчальних курсів. У разі незгоди з умовами цього Договору укладення договору приєднання не є можливим.</p>
