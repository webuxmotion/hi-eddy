      термін дії Договору не продовжується.

      <br>10.2 Виконавець має право в односторонньому порядку розірвати цей Договір у випадку:
      <br>10.2.1 невиконання Стороною своїх обов’язків за цим Договором;
      <br>10.2.2 у випадках передбачених розділом цього Договору.</p>

