      онлайн трансляції відповідно до Програми курсу.

      <br>5.4 Моментом виконання надання послуг вважається передача доступу до Особистого кабінету 
      Користувача. Користувач споживає послуги шляхом перегляду Навчальних матеріалів розміщених 
