      <br>1.1 Платформа HI-EDDY – набір навчальних матеріалів, що рекламується в мережі Інтернет, 
      та розміщуються та адмініструються Виконавцем за допомогою спеціалізованого програмного середовища 
      для проходження онлайн курсів на сайті hi-eddy.com (далі – Платформа). 
      Виконавець може на власний розсуд змінювати Платформу (спеціалізоване програмне забезпечення) 
      на інше спеціалізоване програмне середовище, при цьому, Користувач повинен самостійно ознайомитися 
      із функціоналом та правилами роботи Платформи. Користування функціями 
      Платформи (програмного середовища) входить у вартість курсу.

      <br>1.2 Курс – сукупність навчальних модулів (відео-матеріалів, завдань, тощо), 
      які складені відповідно до певної тематики та в межах яких надаються послуги.

      <br>1.3 Навчальний матеріал – презентація, інтерактивний матеріал, відеозапис, 
      домашнє завдання тощо, які розміщені на Платформі.

      <br>1.4 Тарифи – відомості про ціну Курсу, умови надання знижок, інших корисних переваг, 
      які зазначені на вебсайті, фейсбук сторінці HI-EDDY чи рекламних матеріалах.
      <br>1.5 Особистий кабінет – обліковий запис Користувача на Платформі, який адмініструється 
      Виконавцем і через який Користувач отримує доступ до Курсів, Навчальних матеріалів, 
      іншу необхідну для виконання договору інформацію.</p>

    <p>2. ПРЕДМЕТ ДОГОВОРУ
      <br>2.1 Виконавець зобов’язується надати Користувачу послуги, зміст яких полягає в доступі 
      Користувача до Особистого кабінету Платформи та забезпеченні можливості доступу Користувача 
      через його Особистий кабінет до відповідних Навчальних матеріалів певного онлайн Курсу 
      протягом дії договору (10 днів, або 30 днів, або 365 днів), а Користувач – прийняти і оплатити 
      послуги на умовах, визначених в цій оферті. Вартість конкретного Курсу (навчальних матеріалів)
      визначена на сайті або рекламних матеріалах.</p>

    <p>3. ПОРЯДОК УКЛАДЕННЯ ДОГОВОРУ
      <br>3.1 Цей Договір укладається на умовах договору приєднання (ст. 634 Цивільного кодексу України)
      та вважається укладеним з моменту прийняття публічної оферти.

      <br>3.2 Приймаючи публічну оферту на укладення такого Договору, Користувач підтверджує, 
      що він повністю приймає його умови.

      <br>3.3 Користувач приймає (акцептує) оферту шляхом: здійснення конклюдентних дій – 
      оплати вибраного Тарифного плану на умовах, зазначених у §4 цього Договору. Письмова форма цього 
      Договору згідно ЗУ «Про електронну комерцію» додатково може підтверджуватися направленням
      електронного листа.</p>

    <p>4. ЦІНА ПОСЛУГ ТА ПОРЯДОК РОЗРАХУНКІВ
      <br>4.1 Ціна визначається та застосовується відповідно до обраного Користувачем Тарифного плану, 
      яка вказується на відповідній вебсторінці Тарифного плану та (або) соцмережах чи рекламних матеріалах.

      <br>4.2 Розрахунки за послуги здійснюються шляхом перерахування грошей на рахунок Виконавця 
      чи через онлайн платіжні системи, що використовуються Виконавцем.

      <br>4.3 Оплата послуг здійснюється у гривневому еквіваленті згідно встановленого 
      Національним банком України курсу валют станом на день оплати.

      <br>4.4 Після повної або часткової оплати кошти не повертаються. 

      <br>4.5 Користувач не може вимагати зниження вартості послуг чи повернення коштів, у випадку, 
      якщо він не скористався послугами. Моментом виконання надання послуг вважається передача доступу 
      до Особистого кабінету Користувача.
      
