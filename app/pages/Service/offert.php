    <p>10. СТРОК ДІЇ ДОГОВОРУ
      <br>10.1 Договір діє до моменту спливу одного з періодів (10 днів, або 30 днів, або 365 днів) 
      від дня початку Курсу відповідно до Програми Курсу. Доступ до Платформи та Навчальних матеріалів 
      надається лише на термін дії Договору. 
